const Excel = require("exceljs");

function counter (arr) {
  let acc = []
  let countedNames = arr.reduce(function (allNames, name) { 
    if (name in allNames) {
      allNames[name]++;
    }
    else {
      allNames[name] = 1;
    }
    return allNames;
  }, {});

  for (const [key, value] of Object.entries(countedNames)) {
    acc.push({ name: key, count: value, percent: `${( value / 3 ).toFixed(2)}%` })
  }

  return acc
}

